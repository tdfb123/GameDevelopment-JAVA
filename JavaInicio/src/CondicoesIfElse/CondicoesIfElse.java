package CondicoesIfElse;

public class CondicoesIfElse {
    public static void main(String[] args) {

        String nome = "Thiago";

        if(nome == "Thiago") {
            System.out.println("O nome dele é: " + nome);
        }else if(nome == "Fernando") {
            System.out.println("O nome dele é: " + nome);
        }else {
            System.out.println("Não sabemos o nome dele.");
        }
    }
}
